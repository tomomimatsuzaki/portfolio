            <div class="title-box">
                <h3 class="small-title" style="margin-top:40px;">Critiques & Revision</h3> 
            </div> 
            <p>I had an in-class peer critique session in HCDE 526 class and received feedback on my storyboard and shot lists from 3 classmates as well as my professor. Here are some key design decisions that I made:</p>
            
            <h4 class="smaller-heading">Trajectory</h4>
            <p>I mapped out the story in a way that it effectively took the audience to an emotional journey and kept the story fun and interesting. I utilized the <mark>three-act structure (context, problem, resolution)</mark> to increase the audience engagement and drive their emotions and actions.</p>
            <h4 class="smaller-heading">Less is More</h4>
            <p style="margin-bottom:30px;">The beginning of the story was taking too long to show the context and problem space. This made the audience less engaged before introducing Grow Together as a solution. By <mark>minimizing the time to reveal the problem space and moving on to the solution more quickly</mark>, I was able to keep the audience's attention throughout the entire video.</p>
            <h4 class="smaller-heading">Cuts and Transitions</h4>
            <p style="margin-bottom:30px;">I also found it crucial to employ the best cut and transition effects to enhance the story while keeping the audience engaged during a scene change. Especially, J-cut and L-cut effects were helpful to achieve smooth visual flow.</p>
